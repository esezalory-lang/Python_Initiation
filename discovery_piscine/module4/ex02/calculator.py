#!/bin/python3

frst = int(input("Give me the first number: "))
scnd = int(input("Give me the second number: "))
print("Thank you!", f"{frst} + {scnd} = {frst + scnd}")
print(f"{frst} - {scnd} = {frst - scnd}")
print(f"{frst} / {scnd} = {int(frst / scnd)}")
print(f"{frst} * {scnd} = {frst * scnd}")
